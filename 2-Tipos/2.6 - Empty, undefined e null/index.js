// Empty
// Variavel foi declarada mas sem valor
let pera = "";
console.log("Empty(Pêra): " + pera);
console.log("Valor é verdadeiro? " + (pera === true));
console.log("Valor é falso? " + (pera === false));
//Null
// quando você quer que o valor seja nulo
let maca = null;
console.log("Null(maça): " + maca);
console.log("Valor é verdadeiro? " + (maca === true));
console.log("Valor é falso? " + (maca === false));
// Undefined
// Não existe nada
//ex1
let abacaxi;
console.log("Undefined(abacaxi): " + abacaxi);
